"""
Build GASTON-native isodepth/SNAI1-ac score alignment files.

This step is downstream of GASTON training. It does not retrain GASTON and does
not overwrite source h5ad files. For each accepted whole-tissue GASTON sample it
aligns:

- GASTON isodepth and selected domain labels
- production SNAI1-ac EnrichMap score already present in adata.obs
- two missing SNAI1-ac EnrichMap variants computed in memory

The output is deliberately per-sample. A cohort-sized external spot table can be
derived later, but it is not the primary method contract.
"""

